namespace Glaives.Core.Graphics
{
    /// <summary>
    /// The text alignment anchors the lines to the left, center or right side of the text bounds
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// Align text left
        /// </summary>
        Left,
        /// <summary>
        /// Align text center
        /// </summary>
        Center,
        /// <summary>
        /// Align text right
        /// </summary>
        Right
    }
}